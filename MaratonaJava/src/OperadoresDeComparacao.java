public class OperadoresDeComparacao {

    public static void main(String[] args) {
        //OPERADORES DE COMPARAÇÃO
        // < (<=), (>=) >, == (igualdade), != (diferente)

        boolean dezMaiorQueVinte = 10 >= 20;
        boolean dezMenorIgualVnte = 10 <= 20;

        System.out.println("O operador é: " + dezMaiorQueVinte );
        System.out.println("O operador é: " + dezMenorIgualVnte );
        System.out.println("O operador é: " + (5==5));
        System.out.println("O operador é: " + (5 != 5));
    }
}
